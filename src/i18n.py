class I18n:
    """Internationalization manager"""
    
    _instance = None
    _current_lang = "en"
    
    def __new__(cls):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
            cls._instance._init()
        return cls._instance
    
    def _init(self):
        self._current_lang = self._detect_system_language()
    
    def _detect_system_language(self) -> str:
        try:
            system_locale = locale.getdefaultlocale()[0]
            if system_locale:
                lang_code = system_locale.split('_')[0].lower()
                if lang_code in TRANSLATIONS:
                    return lang_code
        except Exception:
            pass
        return "en"
    
    @property
    def current_language(self) -> str:
        return self._current_lang
    
    @current_language.setter
    def current_language(self, lang: str):
        if lang in TRANSLATIONS:
            self._current_lang = lang
    
    def get(self, key: str, *args) -> str:
        translations = TRANSLATIONS.get(self._current_lang, TRANSLATIONS["en"])
        text = translations.get(key, TRANSLATIONS["en"].get(key, key))
        if args:
            try:
                text = text.format(*args)
            except (IndexError, KeyError):
                pass
        return text
    
    def __call__(self, key: str, *args) -> str:
        return self.get(key, *args)
    
    @staticmethod
    def get_available_languages() -> Dict[str, str]:
        return LANGUAGES.copy()


# Global instance
i18n = I18n()

def tr(key: str, *args) -> str:
    """Translate string by key"""
    return i18n.get(key, *args)
